#include "libft.h"

int		ft_isalnum(int c)
{
	int res;

	res = 0;
	if ((c > 47 && c < 58)
		|| (c > 64 && c < 91)
		|| (c > 96 && c < 123))
		res = 1;
	return (res);
}
