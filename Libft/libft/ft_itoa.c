#include "libft.h"

char	*ft_itoa(int n)
{
	char	*s;
	int		len;

	len = ft_lennum(n);
	if (!(s = ft_strnew(len)))
		return (NULL);
	if (n >= 0)
	{
		s[--len] = 48 + (n % 10);
		n /= 10;
		while (n > 0)
		{
			s[--len] = 48 + (n % 10);
			n /= 10;
		}
		return (s);
	}
	s[0] = '-';
	while (n)
	{
		s[--len] = 48 - (n % 10);
		n /= 10;
	}
	return (s);
}
